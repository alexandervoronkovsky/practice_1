def upper_word():
    """принимаем на вход строку и возвращает ее со всеми заглавными буквами"""
    string = "hello world"
    print(string.upper())


upper_word()

def title_word():
    """делаем заглавными первые буквы каждого слова в строке """
    string = "hello word"
    print(string.title())


title_word()